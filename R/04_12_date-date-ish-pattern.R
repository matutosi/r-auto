mn_dy <- "(0[1-9]|[12][0-9]|3[01])"        # 月日
yr_2 <-  "([0-9]{2}(0[1-9]|1[0-2])"        # 2桁の年
yr_4 <-  "((19|20)[0-9]{2}(0[1-9]|1[0-2])" # 4桁の年
paste0(      mn_dy, dw) # 月日のみ
paste0(yr_2, mn_dy, dw) # 2桁の年と月日
paste0(yr_4, mn_dy, dw) # 4桁の年と月日

